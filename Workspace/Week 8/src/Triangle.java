
public class Triangle extends Shape {
	private int s1, s2, s3;
	
    public Triangle(String newName, int s1, int s2, int s3) {
        super(newName);
        this.s1 = s1;
        this.s2 = s2;
        this.s3 = s3;
    }

	@Override
	public int perimeter() {
		return this.s1 + this.s2 + this.s3;
	}

}
