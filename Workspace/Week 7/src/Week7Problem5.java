import java.util.ArrayList;


public class Week7Problem5 {
	
	public static ArrayList <Character> firstChars(ArrayList <String> input) {
		ArrayList <Character> output = new ArrayList <Character> (input.size());
		
		for (String s : input) {
			output.add(s.charAt(0));
		}
		
		return output;
	}

	public static void main(String[] args) {
		ArrayList <String> thing = new ArrayList <String> ();
		thing.add("Tungsten");
		thing.add("DKneezel");
		thing.add("Santa Claus");
		thing.add("Four score and seven years ago...");
		
		ArrayList <Character> otherThing = firstChars(thing);
		
		for (Character s : otherThing) {
			System.out.println(s);
		}

	}

}
